    private void updateOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateOrderBtnActionPerformed
        if (Validationcheck) {
            if ((txtCustomerID.getText()).length() != 10) {
                JOptionPane.showMessageDialog(this, "Customer ID must include 10 digits");
                txtCustomerID.requestFocus();
            }
            int res = JOptionPane.showConfirmDialog(this, "Do you want to Update ? ", "Update Order", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {

                Node temp = HomePage.first;

                while (temp != null) {
                    if (ValidationOrderID.equals(temp.order.getBurgerID())) {
                        String customerID = txtCustomerID.getText();
                        String customerName = txtCustomerName.getText();
                        int qty = Integer.parseInt(txtQty.getText());
                        double total = Double.parseDouble(lblTotal.getText());
                        int status = statusJComboBox.getSelectedIndex();

                        temp.order.setCustomerID(customerID);
                        temp.order.setCustomerName(customerName);
                        temp.order.setBurgerQty(qty);
                        temp.order.setTotal(total);
                        temp.order.setStatus(status);
                    }
                    temp = temp.next;
                }

                JOptionPane.showMessageDialog(this, "Order Updated");

                txtOrderID.setText("B");
                txtCustomerID.setText("");
                txtCustomerName.setText("");
                txtQty.setText("");
                lblTotal.setText("0");

            } else {
                JOptionPane.showMessageDialog(this, "Order did not updated");

                txtOrderID.setText("B");
                txtCustomerID.setText("");
                txtCustomerName.setText("");
                txtQty.setText("");
                lblTotal.setText("0");
            }
        }
    }//GEN-LAST:event_updateOrderBtnActionPerformed

