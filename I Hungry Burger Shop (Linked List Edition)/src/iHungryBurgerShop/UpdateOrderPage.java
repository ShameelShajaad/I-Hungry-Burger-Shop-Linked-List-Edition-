    private void txtOrderIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOrderIDKeyReleased
        String orderID = txtOrderID.getText();

        if ((orderID.charAt(0)) != 'B') {
            JOptionPane.showMessageDialog(this, "Order ID must start with 'B'");
            txtOrderID.setText("B");
            return;
        }
        if (orderID.length() > 5) {
            JOptionPane.showMessageDialog(this, "Invalid Order ID length");
            txtOrderID.setText("B");
            return;
        }

        if (orderID.length() == 5) {
            statusJComboBox.setEnabled(true);
            orderExpJLabel1.setText("");
            orderExpJLabel2.setText("");
            txtCustomerID.setEditable(true);
            txtCustomerName.setEditable(true);
            txtQty.setEditable(true);

            Node temp = HomePage.first;
            boolean bool = true;

            while (temp != null) {
                if (orderID.equals(temp.order.getBurgerID())) {
                    ValidationOrderID = temp.order.getBurgerID();
                    Validationcheck = true;
                    txtCustomerID.setText(temp.order.getCustomerID());
                    txtCustomerName.setText(temp.order.getCustomerName());
                    txtQty.setText(Integer.toString(temp.order.getBurgerQty()));
                    double qty = temp.order.getBurgerQty();
                    lblTotal.setText(Double.toString(qty * 500));
                    if (temp.order.getStatus() == 0) {
                        statusJComboBox.setSelectedIndex(0);
                    } else if (temp.order.getStatus() == 1) {
                        statusJComboBox.setSelectedIndex(1);
                        statusJComboBox.setEnabled(false);
                        orderExpJLabel1.setText("This Order has been Delivered");
                        orderExpJLabel2.setText("Sorry, you cannot Update this Order");
                        txtCustomerID.setEditable(false);
                        txtCustomerName.setEditable(false);
                        txtQty.setEditable(false);
                    } else if (temp.order.getStatus() == 2) {
                        statusJComboBox.setSelectedIndex(2);
                        statusJComboBox.setEnabled(false);
                        orderExpJLabel1.setText("This Order has been Cancelled");
                        orderExpJLabel2.setText("Sorry, you cannot Update this Order");
                        txtCustomerID.setEditable(false);
                        txtCustomerName.setEditable(false);
                        txtQty.setEditable(false);
                    }
                    bool = false;
                    break;
                }

                temp = temp.next;
            }

            if (bool) {
                JOptionPane.showMessageDialog(this, "Order ID not found");
                txtOrderID.setText("B");
            }
        }
    }//GEN-LAST:event_txtOrderIDKeyReleased

    private void txtCustomerIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCustomerIDKeyReleased
        String text = txtCustomerID.getText();
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Customer ID cannot be empty");
        } else {
            char ch = txtCustomerID.getText().charAt(0);
            if (ch != '0') {
                JOptionPane.showMessageDialog(null, "Customer ID must start with a '0'");
                txtCustomerID.setText("0");
            }
            if (text.length() > 10) {
                JOptionPane.showMessageDialog(null, "Invalid Customer ID");
                txtCustomerID.setText("0");
            }
        }

        if (text.length() == 10) {
            txtCustomerName.requestFocus();
        }
    }//GEN-LAST:event_txtCustomerIDKeyReleased

    private void txtQtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtyKeyReleased
        String text = txtQty.getText();
        char ch = 0;
        boolean check = true;
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Quantity cannot be empty");
            check = false;
        } else {
            for (int i = 0; i < text.length(); i++) {
                ch = txtQty.getText().charAt(i);
                if (ch < '0' || ch > '9') {
                    JOptionPane.showMessageDialog(this, "Cannot enter letteres or symbols in Quantity field");
                    txtQty.setText("");
                    check = false;
                }
            }
            if (check) {
                double qty = Double.parseDouble(txtQty.getText());
                if (qty <= 0) {
                    JOptionPane.showMessageDialog(this, "Quantity must be greater than 0");
                } else {
                    lblTotal.setText(Double.toString(qty * 500));
                }
            }
        }
    }//GEN-LAST:event_txtQtyKeyReleased

