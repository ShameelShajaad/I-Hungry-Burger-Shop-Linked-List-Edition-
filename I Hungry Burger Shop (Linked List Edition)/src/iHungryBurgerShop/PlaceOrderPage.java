    private void placeOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeOrderBtnActionPerformed
        if (txtCustomerID.getText().isEmpty() || txtCustomerName.getText().isEmpty() || txtQty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the details");
        } else {
            String orderID = txtOrderID.getText();
            String customerID = txtCustomerID.getText();
            String customerName = txtCustomerName.getText();
            int qty = Integer.parseInt(txtQty.getText());
            int res = JOptionPane.showConfirmDialog(this, "Do you want to add ? ", "New Burger", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                BurgerOrder newOrder = new BurgerOrder();

                newOrder.setBurgerID(orderID);
                newOrder.setCustomerID(customerID);
                newOrder.setCustomerName(customerName);
                newOrder.setBurgerQty(qty);
                newOrder.setTotal(BURGERPRICE * Double.parseDouble(txtQty.getText()));

                Node n1 = new Node(newOrder);
                if (HomePage.first == null) {
                    HomePage.setfirstNode(n1);
                    HomePage.setLastNode(n1);
                } else {
                    HomePage.setLastNodeNext(n1);
                    HomePage.setLastNode(n1);
                }

                JOptionPane.showMessageDialog(this, "Added Success");
                txtOrderID.setText(generateOrderID());
                txtCustomerID.setText("0");
                txtCustomerName.setText("");
                txtQty.setText("");
            }
        }
    }//GEN-LAST:event_placeOrderBtnActionPerformed

