    private void txtQtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtyKeyReleased
        String text = txtQty.getText();
        char ch = 0;
        boolean check = true;
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Quantity cannot be empty");
            check = false;
        } else {
            for (int i = 0; i < text.length(); i++) {
                ch = txtQty.getText().charAt(i);
                if (ch < '0' || ch > '9') {
                    JOptionPane.showMessageDialog(this, "Cannot enter letteres or symbols in Quantity field");
                    txtQty.setText("");
                    check = false;
                }
            }
            if (check) {
                double qty = Double.parseDouble(txtQty.getText());
                if (qty <= 0) {
                    JOptionPane.showMessageDialog(this, "Quantity must be greater than 0");
                } else {
                    lblTotal.setText(Double.toString(qty * BURGERPRICE));
                }
            }
        }
    }//GEN-LAST:event_txtQtyKeyReleased

