    private String generateOrderID() {
        int lastOrderIDNum = 0;
        Node lastNode = HomePage.getLastNode();
        if (lastNode == null) {
            return "B0001";
        }
        String burgerID = lastNode.order.getBurgerID();

        lastOrderIDNum = Integer.parseInt(burgerID.substring(1, 5));

        return String.format("B%04d", 1 + lastOrderIDNum);
    }

