    private void txtCustomerIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCustomerIDKeyReleased
        String text = txtCustomerID.getText();
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Customer ID cannot be empty");
        } else {
            char ch = txtCustomerID.getText().charAt(0);
            if (ch != '0') {
                JOptionPane.showMessageDialog(null, "Customer ID must start with a '0'");
                txtCustomerID.setText("0");
            }
            if (text.length() > 10) {
                JOptionPane.showMessageDialog(null, "Invalid Customer ID");
                txtCustomerID.setText("0");
            }
        }

        if (text.length() == 10) {
            txtCustomerName.setEditable(true);

            Node temp = HomePage.first;

            boolean bool = true;
            while (temp != null) {
                String cusID = temp.order.getCustomerID();

                if (text.equals(cusID)) {
                    txtCustomerName.setText(temp.order.getCustomerName());
                    txtCustomerName.setEditable(false);
                    txtQty.requestFocus();
                    txtQty.setText("1");
                    bool = false;
                }

                temp = temp.next;
            }
            if (bool) {
                txtCustomerName.requestFocus();
            }

        }
    }//GEN-LAST:event_txtCustomerIDKeyReleased

